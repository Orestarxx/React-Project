import React from 'react';
import {useDispatch, useSelector} from "react-redux";
import {useEffect,useState} from "react";

import {genreActions} from "../../../../redux/slices/genreSlice/genre.slice";
import {MysteryBuild} from "./MysteryBuild";
import css from "../../movieStyle.module.css";


const Mystery = () => {
    const {genres: mysteries} = useSelector(state => state.genreReducer)
    const dispatch = useDispatch()
    const [page, setPage] = useState(1)
    useEffect(() => {
        dispatch(genreActions.getByGenreMysteryId({page}))
    }, [page]);
    const nextPage = () => setPage(prev => prev + 1)
    const backPage = () => setPage(prev => prev - 1)
    return (
        <div className={css.holder}>
            {mysteries.results?.map(mystery => <MysteryBuild mystery={mystery} key={mystery.id}/>)}
            <div className={css.buttonHolder}>
                <button className={css.paginationButt} onClick={backPage} disabled={page === 1}>Back</button>
                <button className={css.paginationButt} onClick={nextPage} disabled={!mysteries.results?.length}>Next
                </button>
            </div>
        </div>
    );
};

export {Mystery};