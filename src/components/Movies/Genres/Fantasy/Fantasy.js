import React from 'react';
import {useDispatch, useSelector} from "react-redux";
import {useEffect,useState} from "react";

import {genreActions} from "../../../../redux/slices/genreSlice/genre.slice";
import {FantasyBuild} from "./FantasyBuild";
import css from "../../movieStyle.module.css";


const Fantasy = () => {
    const {genres: fantasies} = useSelector(state => state.genreReducer)
    const dispatch = useDispatch()
    const [page, setPage] = useState(1)
    useEffect(() => {
        dispatch(genreActions.getByGenreFantasyId({page}))
    }, [page]);
    const nextPage = () => setPage(prev => prev + 1)
    const backPage = () => setPage(prev => prev - 1)
    return (
        <div className={css.holder}>
            {fantasies.results?.map(fantasy => <FantasyBuild fantasy={fantasy} key={fantasy.id}/>)}
            <div className={css.buttonHolder}>
                <button className={css.paginationButt} onClick={backPage} disabled={page === 1}>Back</button>
                <button className={css.paginationButt} onClick={nextPage} disabled={!fantasies.results?.length}>Next
                </button>
            </div>
        </div>
    );
};

export {Fantasy};